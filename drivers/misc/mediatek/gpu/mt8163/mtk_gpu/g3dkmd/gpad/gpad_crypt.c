static UINT8  s_prik[4];                            /* private key */
static UINT8  s_seck[GPAD_CRYPT_KEY_SIZE];          /* secret key */
static int    s_seckl = GPAD_CRYPT_KEY_SIZE;        /* secret key len */

int gpad_crypt_update_key(unsigned char *key, int key_len)
{
	int ret = 0;
	UINT8 private_key[GPAD_PRIVATE_KEY_SIZE];

	if (GPAD_CRYPT_KEY_SIZE != key_len) {
		return -1;
	}

	/* use private key and saved public key to compute secret key */
	do {
		get_random_bytes(private_key, GPAD_PRIVATE_KEY_SIZE);
		memcpy(s_prik, private_key, GPAD_PRIVATE_KEY_SIZE);

		/* compute public key and send it back */
		key_len = GPAD_CRYPT_KEY_SIZE;
		DH_PublicKey_Generate(s_gv, sizeof(s_gv), s_pv, sizeof(s_pv), s_prik, sizeof(s_prik), key, &key_len);
	} while (GPAD_CRYPT_KEY_SIZE != key_len);

	/* compute secret key for later use */
	s_seckl = GPAD_CRYPT_KEY_SIZE;
	memset(s_seck, 0, s_seckl);
	DH_SecretKey_Generate(s_saved_pubk, sizeof(s_saved_pubk), s_pv, sizeof(s_pv), s_prik, sizeof(s_prik), s_seck, &s_seckl);

	return ret;
}

int gpad_crypt_decrypt(unsigned char *enc_data, unsigned int enc_size, unsigned char *dec_data, unsigned int dec_size)
{
	unsigned char src[AES_BLOCK_SIZE];
	struct crypto_cipher *tfm = NULL;

	if (enc_size > AES_BLOCK_SIZE) {
		GPAD_LOG_ERR("%s: data size of encrypt(%u) is invalid\n", __func__, enc_size);
		return -EINVAL;
	}
	tfm = crypto_alloc_cipher("aes", 0, CRYPTO_ALG_ASYNC);
	if (IS_ERR(tfm)) {
		GPAD_LOG_ERR("%s: tfm init failed: %ld\n", __func__, PTR_ERR(tfm));
		return -1;
	}

	memset(src, 0, AES_BLOCK_SIZE);
	memcpy(src, enc_data, enc_size);
	crypto_aes_set_key(&tfm->base, s_seck, AES_KEYSIZE_256);
	crypto_cipher_decrypt_one(tfm, dec_data, src);
	crypto_free_cipher(tfm);

	return 0;
}

